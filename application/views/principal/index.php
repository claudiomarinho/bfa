                                <p>O respons&aacute;vel t&eacute;cnico municipal do PBF na Secretaria de Sa&uacute;de deve acessar, na Plataforma <a href="http://egestorab.saude.gov.br" target="_blank">e-Gestor AB</a> &ndash; Sistema Bolsa Fam&iacute;lia (BFA), a rela&ccedil;&atilde;o das fam&iacute;lias benefici&aacute;rias do seu munic&iacute;pio que precisam ser acompanhadas pela sa&uacute;de a cada vig&ecirc;ncia (1&ordf; vig&ecirc;ncia: janeiro a junho. 2&ordf; vig&ecirc;ncia: julho a dezembro).</p>
                                <p>A identifica&ccedil;&atilde;o das gestantes eleg&iacute;veis ao Benef&iacute;cio Vari&aacute;vel a Gestante (BVG) &eacute; feita pela sa&uacute;de. Este benef&iacute;cio objetiva o aumento da prote&ccedil;&atilde;o &agrave; m&atilde;e e ao beb&ecirc;, elevando a renda familiar na gesta&ccedil;&atilde;o e na primeira inf&acirc;ncia, amplia-se a responsabilidade do Sistema &Uacute;nico de Sa&uacute;de junto &agrave;s fam&iacute;lias do PBF. Para isso, &eacute; essencial a capta&ccedil;&atilde;o precoce das benefici&aacute;rias gestantes pelo servi&ccedil;o de sa&uacute;de para a realiza&ccedil;&atilde;o do pr&eacute;-natal. Quanto antes for informado a gesta&ccedil;&atilde;o, mais rapidamente a fam&iacute;lia receber&aacute; o benef&iacute;cio.</p>
                                <p>O registro do acompanhamento das condicionalidades de sa&uacute;de, tanto das mulheres quanto das crian&ccedil;as, tamb&eacute;m deve ser registrado na na Plataforma e-Gestor AB &ndash; Sistema Bolsa Fam&iacute;lia (BFA).</p>
