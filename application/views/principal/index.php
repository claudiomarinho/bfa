                        <div class="box-body">
                            <div class="scrollbar" style="height: 200px!important; overflow-y: scroll;">
                                <p>O <strong>Programa Bolsa Fam&iacute;lia (PBF)</strong> &eacute; um programa federal de transfer&ecirc;ncia direta de renda &agrave; fam&iacute;lias em situa&ccedil;&atilde;o de pobreza ou de extrema, com a finalidade de promover seu acesso aos direitos sociais b&aacute;sicos e romper com o ciclo intergeracional da pobreza. O Programa &eacute; realizado por meio de aux&iacute;lio financeiro vinculado ao cumprimento de compromissos (condicionalidades) na Sa&uacute;de, Educa&ccedil;&atilde;o e Assist&ecirc;ncia Social.</p>
                                <p>O objetivo das condicionalidades do Programa &eacute; garantir a oferta das a&ccedil;&otilde;es b&aacute;sicas, e potencializar a melhoria da qualidade de vida das fam&iacute;lias e contribuir para a sua inclus&atilde;o social.</p>
                                <p>A agenda de sa&uacute;de do PBF no SUS compreende a oferta de servi&ccedil;os para a realiza&ccedil;&atilde;o do pr&eacute;-natal pelas gestantes, o acompanhamento do crescimento e desenvolvimento infantil e imuniza&ccedil;&atilde;o das crian&ccedil;as. Assim, as fam&iacute;lias benefici&aacute;rias do PBF com mulheres com idade entre 14 e 44 anos e crian&ccedil;as menores de sete anos de idade dever&atilde;o ser assistidas por uma equipe de sa&uacute;de da fam&iacute;lia, por agentes comunit&aacute;rios de sa&uacute;de ou por unidades b&aacute;sicas de sa&uacute;de, que prover&atilde;o os servi&ccedil;os necess&aacute;rios ao cumprimento das a&ccedil;&otilde;es de responsabilidade da fam&iacute;lia.</p>
                                <p>A Pol&iacute;tica Nacional de Aten&ccedil;&atilde;o B&aacute;sica (2017) destaca que &eacute; de responsabilidade comum a todos os membros das Equipes que atuam na Aten&ccedil;&atilde;o B&aacute;sica acompanhar e registrar no Sistema de Informa&ccedil;&atilde;o da Aten&ccedil;&atilde;o B&aacute;sica e no mapa de acompanhamento do Programa Bolsa Fam&iacute;lia (PBF) as condicionalidades de sa&uacute;de das fam&iacute;lias benefici&aacute;rias.</p>
